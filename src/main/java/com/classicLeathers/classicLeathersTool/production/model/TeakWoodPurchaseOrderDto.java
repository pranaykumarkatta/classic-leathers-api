package com.classicLeathers.classicLeathersTool.production.model;

public class TeakWoodPurchaseOrderDto {

    private String classicSku;
    private String teakWoodSku;
    private String leather;
    private String handStitchingPattern;
    private String style;
    private String lining;
    private String sole;

    public String getClassicSku() {
        return classicSku;
    }

    public void setClassicSku(String classicSku) {
        this.classicSku = classicSku;
    }

    public String getTeakWoodSku() {
        return teakWoodSku;
    }

    public void setTeakWoodSku(String teakWoodSku) {
        this.teakWoodSku = teakWoodSku;
    }

    public String getLeather() {
        return leather;
    }

    public void setLeather(String leather) {
        this.leather = leather;
    }

    public String getHandStitchingPattern() {
        return handStitchingPattern;
    }

    public void setHandStitchingPattern(String handStitchingPattern) {
        this.handStitchingPattern = handStitchingPattern;
    }

    public String getStyle() {
        return style;
    }

    public void setStyle(String style) {
        this.style = style;
    }

    public String getLining() {
        return lining;
    }

    public void setLining(String lining) {
        this.lining = lining;
    }

    public String getSole() {
        return sole;
    }

    public void setSole(String sole) {
        this.sole = sole;
    }
}
